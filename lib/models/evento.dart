class Evento {
  final String nombre;
  final String fecha;
  final String hora;
  final String duracion;
  final String servicio;
  final bool allDay;
  final String nota;

  Evento({
    required this.nombre,
    required this.fecha,
    required this.hora,
    required this.duracion,
    required this.servicio,
    required this.allDay,
    required this.nota,
  });
}
