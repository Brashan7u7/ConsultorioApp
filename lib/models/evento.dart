class Evento {
  int id;
  String nombre;
  String fecha;
  String hora;
  String duracion;
  String servicio;
  String nota;

  Evento({
    required this.id,
    required this.nombre,
    required this.fecha,
    required this.hora,
    required this.duracion,
    required this.servicio,
    required this.nota,
  });
}